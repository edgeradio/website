  <h1 class="title-head-right">About Edge</h1>
  
  
  
  <h2>What Is Edge Radio?</h2>
          <p>Nothing like Edge Radio has existed before in Hobart. We provide a progressive, interactive community service that has been available in other Australian capital cities for decades.&nbsp; After a 4 week trial period in August 2002, Edge was awarded a permanent community radio licence in Dec 2002. We then commenced permanent broadcast in Feb 2003.</p>
          <p>Edge is committed to enhancing Hobart's culture by providing a credible, not-for-profit service that fills a void in Hobart's airwaves. As Hobart's only youth focused community station, we provide a mix of arts, music, entertainment, local journalistic content and information to enhance Hobart's cultural landscape. Edge broadcasts to the greater Hobart area on 99.3fm, and webstreams globally on our website. </p>
<h2>Who Twiddles The Knobs & Things?</h2>
          <p>Edge Radio's volunteer broadcasters are as diverse as our listeners. Our volunteers share a common goal: to enhance the lives of Hobart's listeners.</p>
          <p>We have over 100 volunteer presenters, producing around 60 radio programs every week. Our volunteers are active representatives from their demographic: they make REAL RADIO that is of interest to their peer group, and are the 'tastemakers' in their community… artists, musicians, promoters, professionals, students, achievers… Edge Radio listeners are responsive. They tune to us to find out what is happening in Hobart and throughout Tasmania, and our presenters keep them connected. Our approach ensures Edge remains a relevant and sustainable media outlet.</p>
          <h2>Who Are The Hip, Young Thangs Listening?</h2>
          <p>Youth of <em>all</em> ages. Hobart has a richly diverse community and Edge Radio recognises the unique needs of it's community. Edge's programming is broad, and designed to reflect the interests of Hobart. This approach caters not only for the core audience, but has seen a substantial audience beyond this community grow. </p>
          <p class="text_red"><strong>The McNair Ingenuity Research survey for Community Radio was conducted <br />
          nationally in 2008</strong></p>
          <p>Community radio listeners in Hobart are regular listeners:</p>
          <ul>
            <li>80% are listening to at least 10 hours of radio a week</li>
            <li>12% are listening to over 20 hours a week</li>
            <li>59% love that we play Australian music and support Tasmanian artists</li>
            <li>52% of listeners love our 'real' approach to radio, citing the announcers sound like ordinary people or 'one of us'</li>
            <li>57% listen for our specialist music programs – we know music</li>
          </ul>
       
          <h2>How Do We Keep Doing This Fun Stuff?</h2>
          <p><strong>Volunteers</strong></p>
          <p>They are our bread and butter. Volunteers create the media...volunteers ARE Edge Radio. Want to be part of the crew? <a href="apply.php">More info here</a>.</p>
<p><strong>Sponsorship</strong></p>
<p>We require sponsorship to pays our bills, meet our budget and keep us on-air. Without this major source of funding, our not-for-profit service could not survive. </p>
<p>We acknowledge our sponsors through on-air announcements, informing listeners of sponsor services. We limit this material to five minutes per hour, creating a core of sponsors for our listeners to support. <a href="sponsor.php">More info here</a>.</p>
              <p><strong>Supporters</strong></p>
              <p>All listeners contribute to the station by enjoying the service and providing feedback. Now you can show your dedication by becoming financial Supporters. Supporting us financially helps improve our service through programming and community events development. <a href="supporter.php">Head on over here</a>. </p>
              <h2>Edge Radio's Mission Statement</h2>
              <p>Our mission as a community radio station is to entertain as well as educate our listening audience. We aim to provide a vibrant, youth focused and broadly inclusive radio station for the Tasmanian community that:</p>
              <ul>
                <li>Enlivens the culture and social climate of Tasmania</li>
                <li>Is high quality</li>
                <li>Is participative</li>
                <li>Has a Tasmanian perspective</li>
                <li> Provides music programming relevant for youth</li>
                <li>Is responsive to the needs of the community  </li>
                <li>Provides a forum to discuss current issues</li>
                <li>Fosters links between the community educational institutions</li>
                <li>Is artistically diverse</li>
                <li>Has informative and educational programming</li>
              </ul>
              <p>Edge Radio has an emphasis on, and actively encourages, local content including current issues that affect Tasmanians, broadcasting local music talent and cultural activities/events. It strives to broadcast fresh, creative, informative and intelligent programming for our listeners.</p>
              <p><strong>Statement of Objectives</strong></p>
              <ul>
                <li>To present original, rich and diverse programming of music and other forms of expression free from the direct constraints of commercial interests.</li>
                <li>To provide the community with a significant alternative to other broadcast media within the station's service area.  </li>
                <li>To provide entertaining music, educational and public affairs programming and information on issues and events of interest to the community. </li>
              </ul>
              <h2>We're Standing On The Edge</h2>
              <p><strong class="text_red">Edge Radio Wins National Broadcasting Award 2006:</strong> Edge Radio's talented presenters and programmers were recognised at the 2006 Community Broadcasting Association of Australia's (CBAA) annual awards. Edge Radio took out the Excellence in Spoken Word Programming Award.</p>
              <p><strong class="text_red">Edge Radio was Recognised as Australia's Community Radio Station of the Year:</strong> At the 2003 CBAA Industry Awards. Less than 12-months into broadcasting, Edge Radio was chosen by the industry voted awards out of over 350 community radio stations nationwide. </p>
              <p><strong class="text_red">Edge Radio's 'School of Rock' received the “Award for Educational Excellence”:</strong> From the Tasmanian Department of Education in 2003 for outstanding youth engagement and youth community service.</p>
              <p> <span class="text_red"><strong>Edge Radio was Awarded Best Contribution to Local Music:</strong></span> At the 2003 CBAA Industry Awards. Edge Radio's commitment to showcasing Tasmanian Music and enhancing the Tasmanian music scene was acknowledged by industry peers</p>
              <p> <span class="text_red"><strong>Edge Radio received Hobart's Volunteer Organisation “Best Practice Award”</strong></span>: At the Hobart City Council 2004 Volunteer Recognition Awards.      </p>
              
