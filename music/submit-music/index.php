<p><span class="title">Sending Edge Radio Something Physical</span>
<br>
Sending us a physical copy of your music is our preferred method. You are welcome to send us CDs, vinyl records and tapes. All physical submissions are guaranteed to be listened to and evaluated. Our postal address is below.<br><br>

<b>Edge Radio, Music Department, Private Bag 44, Hobart 7001</b><br><br>

Important! Make sure you include a bio/media release and contact details, all the usual stuff really. This is especially important for Tasmanian artists because if you don't tell us you're Tasmanian we won't be able to roll out the red carpet.</p>

<p><span class="title">Sending Edge Radio Something Digital</span>
<br>
You can submit your music digitally to us via email, <b>music [at] edgeradio.org.au</b>. We do not currently offer any kind of dropbox service. Once again, make sure you include a bio/media release and contact details. Tasmanian artists are asked to clearly mark their submissions in the subject line with, <b>Tasmanian Music Submission</b>. Doing this means our systems will instantly pickup your email making sure we don't overlook your submission.
<p><span class="title">Sending something to a specific presenter or program</span>
<br>
If you are familiar with our programming you are welcome to send your music to a specific presenter or show. Simply mark it <b>ATT: Name of presenter or program</b>. Example: If you're a Tassie artist, mark it <b>ATT: Tasmusica</b>.  <b>Edge Radio will never give out the private contact information of our presenters!</b> You MUST mark your package to the desired presenter or show via the described method. The music department will pass it on to the right people for you.</p>

<p><span class="title">Enquiries</span>
<br>
If you want to check the status of something you've sent us, email <b>music [at] edgeradio.org.au</b> or check the <a href="http://www.edgeradio.org.au/program/new-music-on-edge-radio/playlists/">New Music</a> Page. All tracks added to Edge Radio's rotating catalogue are listed there. Edge uses the AMRAP Pages system to log our playlists. You can use the "search playlists" function to find out which programs have been playing your music. The Music Department does it's best to get through everything we're sent but it may take several weeks so please be patient. If we like your stuff we'll add it to our catalogue for everyone to enjoy.</p>

<p><span class="title">Interview Requests</span>
<br>
All interview requests are handled by the Programming Department. Information can be found <a href="http://www.edgeradio.org.au/music/interview-request/">here</a>. Please <b>DO NOT</b> send your interview requests to the Music Department. They're busy trying to get through all the music submitted to Edge Radio and often cannot respond in a timely manner to interview requests.
