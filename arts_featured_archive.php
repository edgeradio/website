        <h1 class="greyheader">FEATURED ARTIST</h1>
        <table width="724" border="0" cellspacing="0" cellpadding="5">
          <tr>
            <td valign="top"><div class="text">
              <p><strong>Name:</strong> Jennifer Storer <br />
                <strong>Medium:</strong> Redevelopment Project Manager<br />
                <strong>Podcast:</strong> <a href="http://www.cpod.org.au/download.php?id=946.mp3"></a></p>
              <p><font color="#FF0000"><em>Featured March 2009.</em></font></p>
              </div>
              <div class="newstext">
              <p>So…. there is a bit of  Hoo Hah about whether or not the museum redevelopment is going ahead or not.  Every-one is sidestepping, ducking and weaving due to the economic crisis we are watching unfold. But what effect exactly will this have on the Tasmanian Museum and Art Gallery (TMAG) redevelopment?</p>
              <p> I spoke to Jennifer Storer, the Acting Redevelopment Project Manager about the proposed changes to TMAG, and how the redevelopment would be beneficial not only to the museum but to the public and the state. </p>
              <p>The museum is proposing a 200 million dollar revamp that would double the museum's existing exhibition space, alowing it to bring in major international travelling shows. </p>
              <p>“We haven’t done much capital work since the sixties when the 1966 building went in” says Storer.</p>
              <p>“We have come to a point were we are stuck, we can’t do what modern museums do because of our constraints”.</p>
              <p>And no… she assured me it’s not going to be another Bilbao theme park.  It has been designed to be sensitive to the current building structure and interactive so the public can experience the context of the history of our museum development. </p>
              <p>Speaking with Jennifer was a delight as she is so passionate about the arts and TMAG. I think this is needed in the arts industry…I mean no-one does it for the money, unless they are Jeff Koons of course. </p>
              <p>Jennifer did not comer from a project management background, but rather a professional museum background. She was appointed to this position so that the development could focus on the art and cultural aspects which sometimes get overwhelmed by the architectural during redevelopments. </p>
              <p>“When I deal with the architects I come already with a professional knowledge of what a museum needs,” she says.</p>
              <p>So, what about the impact of the global economy on the museum? Jennifer seemed optimistic, believing it is a perfect opportunity to maximize the “cultural tourism” that will be occurring in Tasmania. She also revealed the project will be done in stages and that the first $30 million has already been committed for the project to start. </p>
              <p>According to Jennifer, the expansion has to happen: the gallery needs it. The gallery is actually not equipped to take large travelling exhibitions and the existing building is not an open and contemporary space. So therefore, the museum cannot afford to wait any longer or it will miss opportunities that will be available with the upcoming MONA Museum and the many other cultural developments currently occurring in Hobart and Tasmania.</p>
              <p>“Contemporary art is quite large and most of our spaces are built for colonial and Victorian styles…we need those large high ceiling spaces and galley to deal with the contemporary art,” she says.</p>
              <p>One of my concerns is that the museum may shut its doors for two years or more, during the construction phase. But Jennifer assured me that they will do their best to keep sections open to the public, moving exhibits around as the development proceeds. This also means the public can experience and see the unfolding architectural development which is interesting in itself. </p>
              <p>Cultural tourism is economically valuable to the state so it is important that Tasmania can become the cultural and natural crown of the commonwealth. … not disappear into dated obscurity. For Tasmania to remain culturally and artistically vibrant the TMAG development is an important step. </p>
              <p>If you wish to know more about the proposed changes and would like to make a comment, just jump onto the TMAG site: www.tmag.tas.gov.au  or go to the museum front foyer. </p>
