              <div class="newstext">
              <p>What have a big squid, manikins, albatrosses and artists got in common…. TMAGgots! </p>
              <p>TMAGgots is a not for profit group of young Tasmanians who are interested in actively supporting the Tasmanian Museum and Art Gallery. </p>
              <p>Arts On Edge talked to the president of TMAGgots, Kelly Eijdenberg, about why it’s so great to be a part of TMAGgots, and what these cool kids get up to. </p>
              <p>Due to a strange lack of interest in TMAG by youth (I mean, someone who isn’t interested by a big squid, needs to be slapped by one), TMAGgots emerged. With over 200 members Kelly and the TMAGgots have really created some interest. </p>
              <p>And you can see why. It’s a great way of getting behind the scenes of the Tasmanian Museum and Art Gallery and seeing all the weird and wonderful collections that are not available to the general public. In fact, 90% of the museum’s collection lies behind locked doors. </p>
              <p>Every month there is something new, whether it’s schmoozing with artists or wrestling with giant squids, its always going to be entertaining.</p>
              <p>To find out more go to www.tmaggots.org.au or check out The Apple, a quarterly publication available at TMAG or any good café. </p>
              <p>But really, the best way to know what’s going on is to join TMAGgots. You get discount invitations to all TMAGgots events, to museums in Australia and internationally, Theatre productions and TSO concerts.</p>
