        <h1 class="greyheader">FEATURED GALLERY/ORGANISATION</h1>
           <table width="724" border="0" cellspacing="0" cellpadding="5">
          <tr>
            <td valign="top"><div class="text">
              <p><strong>Name:</strong> Moonah Arts Centre (MAC)<br />
                Arts &amp; Cultural Development Officer Sean Kelly                <br />
                <strong>Website: </strong><a href="http://www.mac.gcc.tas.gov.au" target="_blank">www.mac.gcc.tas.gov.au</a><br />
                <strong>Podcast:</strong> <a href="http://www.cpod.org.au/download.php?id=1216.mp3"></a></p>
<p><font color="#FF0000"><em>Featured May 2009.</em></font></p>
              </div>
              <div class="newstext">
              <p>Arts on the edge talks to Sean Kelly Arts &amp; Cultural Development Officer Of Moonah Arts Centre (MAC)<br />
              </p>
              <p><strong>“It’s not an industry, it’s a carnival with a casino attached”</strong></p>
              <p>The Moonah Arts Centre is a community based arts centre and is one of those little gems that we all take for granted. It’s been around for a while, first starting as a library that merged into an arts centre as a part of Glenorchy City Council, located just off the main street of Moonah.</p>
              <p>This year Moonah Arts Centre promises to deliver a plethora of events, workshops, artist in residencies, music marathons…I could go on and on, but better you go and pick up the MAC program. In fact, if you ever say you’re bored and have nothing to do you may find me behind you ready to slap you with their arts program.</p>
              <p>Seriously though, this year Sean Kelly, Arts and Cultural Development Officer at MAC, promises a jam-packed year with a key focus on local youth and community.</p>
              <p>“We have a ridiculously busy program, we have up to 14 exhibitions a year, workshops and concerts, we try to cover as many different art forms as we can so it’s not all just crafty, we look for new things all the time...fresh art and fresh ideas,&quot; says Sean.</p>
              <p>March kicks off with an exciting new project called STIGMA Research Lab.  The project incorporates a team of artists in residency at the Moonah gallery, transforming it into a creative hub for the month. Moonah Arts Centre invites anyone and everyone to contribute. The outcome will be a series of site-specific installations in Hobart and Glenorchy.</p>
              <p>“The artists who are working on the project will work out in the community but will be based at the centre. That will mean people can come in and engage with what they’re doing, and what they’re doing is around the idea of social stigma, stigma based on were we live. If you come from a housing commission area, public housing area, what does that mean and how do people regard you and how realistic are these stereotypes...you can come in and talk to the artist and give them information,” explains Sean.</p>
              <p>The project is collaboration between the University of Tasmania, CAST and the Glenorchy City Council through the Moonah Arts Centre, and of course you, so get involved. </p>
              <p>However if you want to get involved in other ways, like setting up exhibitions, putting together workshops or researching and developing projects, MAC are always looking for new and fresh ideas to be injected into the gallery.</p>
              <p>“It’s a good way to get skilled in how arts operates, whether it’s arts administration or it’s project management or the physical side of installing and theses kind of issues. It’s a good way to get skilled up and if you want to go on and do more in the arts industry… ahh industry I said that word… like Adam Gopnik said ‘it’s not an industry it’s a carnival with a casino attached’. That’s what it is, so if you want to get involved in the carnival, it is a good way to do it because there is not a lot of courses in these things,” says Sean. </p>
              <p>Volunteering is a great way to get involved and get skilled up and confident as well as meeting like-minded people. Get in contact with Sean Kelly or better still get down to the Moonah Arts Centre and chat to one of the many friendly staff. </p>
              <p>The Moonah Arts Centre is open weekdays 12:30 - 5pm and on Saturdays 10am - 2pm if desired by the exhibitor.            </p>
