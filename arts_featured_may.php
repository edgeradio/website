        <h1 class="greyheader">FEATURED ARTIST</h1>
        <table width="724" border="0" cellspacing="0" cellpadding="5">
          <tr>
            <td valign="top"><div class="text">
              <p><strong>Name:</strong>  Brigita Ozolins<br />
                <strong>Medium:</strong> Artist<br />
                <strong>Website:</strong><a href="http://brigitaozolins.com" target="_blank">brigitaozolins.com</a><br />
                <strong>Podcast:</strong> <a href="http://www.cpod.org.au/download.php?id=1215.mp3"></a></p>
              <p><font color="#FF0000"><em>Featured May 2009.</em></font></p>
              </div>
              <div class="newstext">
              <p>It’s hard to know why anyone wants to be an artist. Slaving themselves for a less than adequate pay check, working in a million jobs just to pay for the love and addiction to the craft. I ask myself “why do I do it?” every day. I should have been a public servant, fire fighter, nurse (please insert ‘real’ job here)... I’m always reconsidering. </p>
              <p>However, after meeting Tasmanian artist Brigita Ozolins, a performance artist, artist and writer, it all became clear. </p>
              <p>Intelligent and articulate, but not arrogant, she is the sort of person I would say,<br />
                “I want to be like her when I grow up”. </p>
<p>Brigita has had a fascinating life. Of Latvian origin, she grew up not speaking any English until she started school, believing as a child that everyone spoke the intimate language of Latvian at home and English was language used for the “outside world”. Fascinated with her father’s book she would spend hours in his library pretending to read, sitting, gazing, hoping the mysterious patterns of black and white print would reveal themselves if she stared at it long enough. </p>
              <p>This extraordinary relationship with words and language had a strong impact on her sense of self, her passions and her direction.</p>
              <p>After studying the classics at Monash University she returned to Tasmania and began working as a librarian for the Glenorchy City Council. She continued there until becoming the Arts and Cultural Development Officer, and later, Founder, of Moonah Arts Centre. It wasn’t until a tragic accident, a fall, altered her life, and at the age of forty, she decided to enrol in a fine arts degree.<br />
                <br />
                “When I went to art school I couldn’t believe how fantastic it was. I was so excited it was like I had forty years of art I had to blurt out. I couldn’t stop making work, it was unbelievable. I have a lot more discrimination about what I make now,” she says laughing.</p>
              <p>Armed with a thirst for knowledge and a love for making, Brigita finished her PhD in 2004 and since then has not stopped making, writing and performing.</p>
              <p>“My great passion for making art, and most of my artwork, somehow always links back to our relationship with language to books to libraries and the way we sort information as well as a fascination with bureaucratic systems,” she says.</p>
              <p>Her most recent exhibition Codex was a powerful installation of 756 convex mirrors written in large cryptic letters on painted black walls of the Carnegie Gallery to spell out the text:</p>
              <p>“The history of all man kind is written within ourselves”</p>
              <p>This amazing and immersive exhibition was well received by most with some saying that it was of international standard. However, she was surprised at the more creative responses written in the comments book.</p>
              <p>“There were a lot of people who hated the work, who cannot stand it at all…I must admit I felt a bit sad…when you have THIS IS SHIT in mirror writing…well you sort of laugh at it…I suppose at least they made the effort,” says Brigita. </p>
              <p>If you missed this exhibition, don’t fret, her work is on permanent display in the State Library, the UTAS art school library, and if you are in Latvia she has some work in the National Library - all of which are perfect places to house the work of Brigita Ozolins.</p>
              <p>“I think our relationship to language is really paradoxical. I think that on the one hand it is an amazing tool that enables us to describe ourselves and our place in the world and to share our thoughts and feelings to map history and all those things, but on the other hand it’s not a mirror of reality, not an accurate reflection, that language both shapes who we are but also restricts us,” says Brigita.</p>
              <p>Her studio is testament to her love of knowledge and books. It looks like a library, with shelf after shelf of eclectic works by the great authors, philosophers and poets, as well as her father’s books. </p>
              <p>“I’m a sucker, I need to have the book as an object, and some books are so beautiful”</p>
              <p>Brigita Ozolins makes me realise that being an artist, whether that’s a musician, writer, visual artist or performer, is all about the need to express, the thirst for knowledge and the love that cannot be tamed. </p>
              <p>. </p>
