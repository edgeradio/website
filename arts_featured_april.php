            <td valign="top"><div class="text">
              <p><strong>Name:</strong> Matt Coyle<br />
                <strong>Medium:</strong> Drawing<br />
                <strong>Podcast:</strong> <a href="http://www.cpod.org.au/download.php?id=1062.mp3"></a></p>
              <p><font color="#FF0000"><em>Featured April 2009.</em></font></p>
              </div>
              <div class="newstext">
              <p>As part of Ten Days on the Island, Matt Coyle is exhibiting prints from his book “Worry Doll” at the historic hospital for the insane at Willow Court in New Norfolk. He invites you to experience his work in a unique walk-through installation on the walls of this historic lunatic asylum. </p>
              <p><em>Worry Doll</em> is a dark and frightening story about three toys that are witness to the gruesome murder of their host family. They flee the crime tripping from one place to the next in a misfortunate journey through a nightmare dream carrying with them the weight of their worries.</p>
              <p>“Do you think I have a problem? I mean, I know I’m a little introverted but it’s not like it’s anything major. Sometimes though - and you’ll be pleased to know this isn’t one of those times - I get a real urge to lash out and punch the person I am talking to. It’s like they’re talking to me and all I can think of is what’s holding me back? Everyone gets a bit like that though, don’t you think.”<br />
                (Exert from Matt Coyle’s book <em>Worry Doll</em>)</p>
              <p>His book is creepy, but put it in the historic insane asylum and then you have nightmare material. Willow Court is a perfect place to house the work of Matt Coyle.</p>
              <p>“Willow Court has a strong and at times terrible history, there is alot of power in the place,” says Matt.<br />
                To be honest I was a bit apprehensive about doing an interview with an obsessive compulsive artist who works only with black felt tip pens, spending up to seventy hours making photo realistic drawings of murder scenes, deranged dolls and unsettling horror.<br />
                However I was greeted with smiles and the smell of hot cup cakes cooking, from his family. I scanned the house for traps.</p>
              <p>His studio is modest and small and like most artists studios, organised chaos. Props for his graphic novels lay on the ground, dismantled dolls, stuffed toys, stacks of books and boxes upon boxes of black felt tip pens. <br />
                “I strictly work in black pen…. It’s the love of the craft of drawing the love of the process…. I come more from a painting background, my interest in doing graphic work is based on combining a sequence of individual drawings,” he says.<br />
                These intricate, methodical and remarkable drawings are created late at night after his family goes to bed, the coffee pot goes on, death metal plays, and so does his wild and chilling imagination.</p>
              <p>WORRY DOLL @ WILLOW COURT is a part of Ten Days on the Island, and is a free event on at Willow Court, New Norfolk till April 12th, 10am – 4pm daily. </p>
