        <h1 class="greyheader">FEATURED GALLERY/ORGANISATION</h1>
           <table width="724" border="0" cellspacing="0" cellpadding="5">
          <tr>
            <td valign="top"><div class="text">
              <p><strong>Name:</strong> Elizabeth Walsh<br />
                Artistic Director of Ten Days On The Island<br />
                <strong>Website:</strong><a href="http://www.tendaysontheisland.org" target="_blank">www.tendaysontheisland.org</a><br />
                <strong>Podcast:</strong> <a href="http://www.cpod.org.au/download.php?id=1061.mp3"></a></p>
<p><font color="#FF0000"><em>Featured April 2009.</em></font></p>
              </div>
              <div class="newstext">
              <p>It’s not every day you meet someone who has run away to join the&nbsp;circus, travelled the world in search of treasure and is responsible for shaking Tasmania up every two years with the international art event Ten Days On The Island.</p>
              <p>Arts on the Edge spoke with Elizabeth Walsh the Artistic Director of Ten Days On The Island, about what it is like, juggling Tasmania's renowned biennial arts festival, and what treasures she has found from Aotearoa/New Zealand, Iceland, Taiwan, England, Scotland and of course Tasmania.</p>
              <p>Ten Days on the Island is an extravaganza of music, performance, and my favourite the visual arts.&nbsp; Tasmania is saturated with art from all around Australia and the world. You’ll trip over it as it spills into the streets and this year into more obscure places, like Willow Court in New Norfolk, the first ‘insane asylums’ that operated as part of the Royal Derwent Psychiatric Hospital until 2000, chosen for Matt Coyle’s installation “Worry doll”. As well as works by well known artists in five heritage homes around Tasmania&nbsp;as a part of the exhibition “Trust”.</p>
              <p>“Trust is a project that has taken six years to evolve. It is the result of the partnership between the national trust of Australia, The University of Tasmanian School of Art and Ten Days On The Island. It grew out of a project in 2007 called the Port Arthur project.”</p>
              <p>“ I am very interested that as a place we hold some of the nation’s most significant built heritage and how contemporary we as Tasmanians vision that”, says Walsh.</p>
              <p>If there is one thing you need in the arts industry it is passion, and Elizabeth Welsh is a whirlwind of it, you can’t help but get swept up in her enthusiasm. &nbsp;She runs at a million miles an hour, never stopping, but always smelling the roses. &nbsp;</p>
              <p>“It’s been fantastic, I’ve always followed my heart in terms of what I’ve wanted to engage with culturally.” She says.</p>
              <p>She has been artistic director for Ten Days since 2001, and has watched the event grow and evolve. Don’t ask her what her favourite event is for 2009, she loves all of them like children, all unique and beautiful in their own way. </p>
              <p>“There are things I get excited about for different reasons.” She says.</p>
              <p>Elizabeth has reasons to get excited, this year 2009 Ten Days of the Island will blow you away, for more information pick up a blue program guide from any gallery, TMAG or good café, or simply go to www.tendaysontheisland.org</p>
              <p>Better still listen to Arts on the Edge every Sunday between 4-6pm and we’ll tell you what’s going on.</p>
