<p>Tasmanian Youth Broadcasters Inc. (TYB Inc.) is the incorporated association and licencee of Edge Radio 99.3FM, as provided under licence from the Australian Communications and Media Authority. In simple terms, Edge Radio is the trading name for TYB Inc.</p>

<p>The Committee of Management is the highest body of Edge Radio. It is responsible for the governance and strategic planning of the station. Membership is made up of the President, Vice-President, Secretary and Treasurer, six ordinary members, all of whom are elected at the Annual General Meeting, plus a staff representative. There are two community representative positions and two education representative positions on the committee.</p>

<p>All station broadcasters and staff are constitutionally required to be members of TYB Inc and members of the community are also invited to become members.</p>

<p>Membership costs $20 (incl GST) and lasts until December 31st in that year.</p> 

<h1 class="greyheader">What is the difference between being a member and being a supporter?</h1>

<p>- Membership allows voting rights at the Annual General Meeting and the ability to nominate for position on the Committee of Management.</p>

<p>- Being a supporter is a financial contribution to the station that gives you access to exclusive supporter only give-aways of tickets, CDs and other goodies. Plus the feel-good factor that comes with knowing you're supporting the sustainability of local arts and culture. More information <a href="http://www.edgeradio.org.au/supporter.php">here</a>.</p>


